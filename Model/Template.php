namespace Pecserke\Bundle\TwigDoctrineLoaderBundle\Model;

/**
 * Represents the Twig template data.
 *
 * @author Tomas Pecserke <tomas@pecserke.eu>
 */
class Template
{
    /**
     * @var mixed
     */
    protected $id;

    /**
     * @var string
     */
    protected $name;

    /**
     * @var string
     */
    protected $source;

    /**
     * @var \DateTime
     */
    protected $modifiedAt;

    /**
     * @return mixed
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     * @return $this
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * @return string
     */
    public function getSource()
    {
        return $this->source;
    }

    /**
     * @param string $source
     * @return $this
     */
    public function setSource($source)
    {
        $this->source = $source;

        return $this;
    }

    /**
     * @return \DateTime
     */
    public function getModifiedAt()
    {
        return $this->modifiedAt;
    }

    /**
     * Lifecycle callback updating last modification time.
     */
    public function onModified()
    {
        $this->modifiedAt = new \DateTime();
    }
}
